def mergeArrays(nums1, nums2):
    res = []    
    n1, n2 = 0, 0
    while n1 < len(nums1) or n2 < len(nums2):
        if n1 < len(nums1):
            id1, val1 = nums1[n1]
        else: id1 = float('inf')
        if n2 < len(nums2):
            id2, val2 = nums2[n2]
        else: id2 = float('inf')
        
        if id1 == id2:
            res.append([id1, val1 + val2])
            n1 += 1
            n2 += 1
        elif id1 < id2:
            res.append([id1, val1])
            n1 += 1
        else:
            res.append([id2, val2])
            n2 += 1
    return res

